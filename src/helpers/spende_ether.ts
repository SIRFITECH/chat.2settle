export const getSpendContract = async () => {
  if (typeof window.ethereum !== "undefined") {
    const provider = new BrowserProvider(window.ethereum);
    const signer = await provider.getSigner();
    return new Contract(spendContractAddress, spendAbi, signer);
  } else {
    console.error("MetaMask is not installed");
  }
};

export const setSpender = async (spender: any) => {
  const contract = await getSpendContract();

  if (contract) {
    try {
      const response = await axios.post("/api/set_spender", {
        spender: spender,
      });

      if (response.status === 200) {
        console.log(response.data.message);
      } else {
        console.error(response.data.error);
      }
    } catch (error) {
      console.error("Error setting spender:", error);
    }
  }
};


export const approveAmount = async (amount: string) => {
  const contract = await getSpendContract();
  if (contract) {
    // Parse the amount to Wei (assuming the contract expects Wei)
    const amountInWei = ethers.parseEther(amount);

    // Call the approve function with the amount and pass msg.value in the options
    const tx = await contract.approve(amountInWei, { value: amountInWei });
    await tx.wait();
    // const tx = await contract.approve(ethers.parseEther(amount));
    // await tx.wait();
    console.log("Approval successful");
  }
};

// Function to transfer approved ETH
export const transferTokens = async (from: any, to: any, amount: string) => {
  const contract = await getSpendContract();
  if (contract) {
    // Parse the amount to Wei (assuming the contract expects Wei)
    const amountInWei = ethers.parseEther(amount);

    // Call the transferFrom function without the value parameter
    const tx = await contract.transferFrom(from, to, amountInWei);
    await tx.wait();

    console.log("Transfer successful");
  }
};

export const generateAlias = (publicKey: string) => {
  // Example: Take the first 8 characters of a SHA-256 hash of the public key
  const crypto = require("crypto");
  const hash = crypto.createHash("sha256").update(publicKey).digest("hex");
  return `0x0${hash.substring(0, 39)}`;
};
